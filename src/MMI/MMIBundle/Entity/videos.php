<?php

namespace MMI\MMIBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * videos
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="MMI\MMIBundle\Entity\videosRepository")
 */
class videos
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nom", type="string", length=255)
     */
    private $nom;

    /**
     * @var string
     *
     * @ORM\Column(name="description", type="string", length=255)
     */
    private $description;

    /**
     * @var string
     *
     * @ORM\Column(name="Playlist", type="string", length=255)
     */
    private $Playlist;

    /**
     * @var \DateTime
     *
     * @ORM\Column(name="time", type="datetime")
     */
    private $time;

    /**
     * @var string
     *
     * @ORM\Column(name="auteur", type="string", length=255)
     */
    private $auteur;

    /**
     * @var string
     *
     * @ORM\Column(name="link", type="string", length=255)
     */
    private $link;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nom
     *
     * @param string $nom
     * @return videos
     */
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get nom
     *
     * @return string 
     */
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * Set description
     *
     * @param string $description
     * @return videos
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string 
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set Playlist
     *
     * @param string $Playlist
     * @return videos
     */
    public function setPlyalist($Playlist)
    {
        $this->Playlist = $Playlist;

        return $this;
    }

    /**
     * Get Playlist
     *
     * @return string 
     */
    public function getPlaylist()
    {
        return $this->Playlist;
    }

    /**
     * Set time
     *
     * @param \time $time
     * @return videos
     */
    public function setTime($time)
    {
        $this->time = $time;

        return $this;
    }

    /**
     * Get time
     *
     * @return \DateTime 
     */
    public function getTime()
    {
        return $this->time;
    }

    /**
     * Set auteur
     *
     * @param string $auteur
     * @return videos
     */
    public function setAuteur($auteur)
    {
        $this->auteur = $auteur;

        return $this;
    }

    /**
     * Get auteur
     *
     * @return string 
     */
    public function getAuteur()
    {
        return $this->auteur;
    }

    /**
     * Set link
     *
     * @param string $link
     * @return videos
     */
    public function setLink($link)
    {
        $this->link = $link;

        return $this;
    }

    /**
     * Get link
     *
     * @return string 
     */
    public function getLink()
    {
        return $this->link;
    }

    /**
     * Set Playlist
     *
     * @param string $playlist
     * @return videos
     */
    public function setPlaylist($playlist)
    {
        $this->Playlist = $playlist;

        return $this;
    }
}
